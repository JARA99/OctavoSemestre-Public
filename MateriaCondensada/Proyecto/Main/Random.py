fila = []

def rnd():
    return format(np.power(int(A),random.random()),".2f")

for i in range(int(f)):
    fila.append("1, 1,")
    for j in range(int(c)-1):
        # fila.append(',')
        fila.append(str(rnd())+",")
        # print(rnd())
    fila.append(rnd())
